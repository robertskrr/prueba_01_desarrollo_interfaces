    private void jButtonEliminarCiudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarCiudadActionPerformed
        if (LogicaVisitas.getListaVisitas().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay registros para poder eliminar", "NO REGISTROS", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String ciudadBorrar = JOptionPane.showInputDialog(this, "Introduce el nombre de la ciudad a eliminar", "Eliminar ciudad", JOptionPane.QUESTION_MESSAGE);

        if (ciudadBorrar == null) {
            JOptionPane.showMessageDialog(this, "Operación cancelada", "Cancelado", JOptionPane.INFORMATION_MESSAGE);
            return;
        } else if (ciudadBorrar.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No has introducido ningún nombre", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Visita visitaBorrar = LogicaVisitas.getListaVisitas().stream()
                .filter(v -> v.getCiudad().equalsIgnoreCase(ciudadBorrar))
                .findFirst().orElse(null);
        if (visitaBorrar == null) {
            JOptionPane.showMessageDialog(this, "No hay ninguna coincidencia con la ciudad: " + ciudadBorrar, "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la ciudad " + visitaBorrar.getCiudad() + "?", "¿SEGURO?", JOptionPane.YES_NO_OPTION);

        if (confirmacion == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "De acuerdo, eliminamos la visita a la ciudad " + visitaBorrar.getCiudad() + " en la estación " + visitaBorrar.getEstacion(), "Eliminación de visita", JOptionPane.INFORMATION_MESSAGE);
            LogicaVisitas.getListaVisitas().remove(visitaBorrar);
            refrescarDatosTabla();
        } else {
            JOptionPane.showMessageDialog(this, "Sabía que no te querías perder la visita a la ciudad " + visitaBorrar.getCiudad() + " en la fecha " + sdf.format(visitaBorrar.getFechaPrevista()), "Eliminación de visita cancelada", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButtonEliminarCiudadActionPerformed

