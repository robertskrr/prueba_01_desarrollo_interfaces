    /**
     * Inicializa los componentes de la tabla y su ordenación
     */
    private void inicializarTabla() {
        // Inicializar dtm, asegurando que ordena bien al ser un dato Double
        dtm = new DefaultTableModel() {
            @Override
            public Class getColumnClass(int columna) {
                if (columna != 5) {
                } else {
                    return Double.class;
                }
                return Object.class;
            }
        };

        String[] titulos = new String[]{"Ciudad", "Fecha", "Estación", "Tº mín", "Tº máx", "Tº promedio"};

        dtm.setColumnIdentifiers(titulos);

        jTable.setModel(dtm);

        // Sorter
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTable.getModel());

        jTable.setRowSorter(sorter);

        // Configurar orden
        List<RowSorter.SortKey> sortKeys = new ArrayList<>();
        // De mayor a menor

        sortKeys.add(
                new RowSorter.SortKey(5, SortOrder.DESCENDING));

        // Aplicamos orden
        sorter.setSortKeys(sortKeys);

        // Refrescamos los datos
        refrescarDatosTabla();
    }

    /**
     * Refresca los datos de la tabla
     */
    private void refrescarDatosTabla() {
        dtm.setRowCount(0);

        // Recoge los datos de la lista
        LogicaVisitas.getListaVisitas().forEach(v -> dtm.addRow(new Object[]{v.getCiudad(), sdf.format(v.getFechaPrevista()), v.getEstacion(), v.getTempMin(), v.getTempMax(), v.getTempPromedio()}));

        // Forzar la reordenación
        RowSorter<? extends TableModel> sorter = jTable.getRowSorter();
        if (sorter != null) {
            ((TableRowSorter<?>) sorter).sort();
        }

        // AÑADIR IMAGEN DE ESTACIÓN ÚLTIMA VISITA
        if (LogicaVisitas.getListaVisitas().isEmpty()) {
            return;
        }

        Visita ultimaVisita = LogicaVisitas.getListaVisitas().getLast();
        ImageIcon imgOriginal = new ImageIcon(getClass().getResource("/gui/imgs/" + ultimaVisita.getEstacion().toLowerCase() + ".png"));

        Image img = imgOriginal.getImage();
        // Escala la imagen al tamaño del jLabel
        Image scaledImg = img.getScaledInstance(50, 50, java.awt.Image.SCALE_SMOOTH);

        jLabelImgEstacion.setIcon(new ImageIcon(scaledImg));
    }

